class Operators 
{
	public static void main(String[] args) 
	{
		System.out.println(236/10);		// 23
		System.out.println(236.0/10);	// 23.6
		System.out.println(1/2*3*4);	// 0
		System.out.println(25%3);		// 1
		System.out.println(236%10);		// 6
		System.out.println(236%100);	// 36

	}
}
