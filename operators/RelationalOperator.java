class RelationalOperator 
{
	public static void main(String[] args) 
	{
		int a=13;
		int b=69;

		System.out.println(a<b);	//true
		System.out.println(a>b);	//false
		System.out.println(a<=b);	//true
		System.out.println(a>=b);	//false
		System.out.println(a==b);	//false
		System.out.println(a!=b);	//true
		
	}
}
